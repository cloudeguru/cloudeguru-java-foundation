package com.cloudeguru.unary_operators;

public class UnrayOperators {

	public static void main(String[] args) {
		int a = 10;
		a++; // a= a+1
		System.out.println(a);// 11
		a--; // a= a-1
		System.out.println(a);//10
		
		int b = 10;
		++b; // b = b+1
		System.out.println(b);//11
		--b; // b = b-1
		System.out.println(b);//10

	}

}
